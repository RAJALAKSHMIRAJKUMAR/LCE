DROP PROCEDURE IF EXISTS SP_TENDER_SUMMARY_UPDATE;
CREATE PROCEDURE SP_TENDER_SUMMARY_UPDATE(TMID INTEGER,MARKDOWN INTEGER,USERSTAMP VARCHAR(50),OUT SUCCESS_MESSAGE INTEGER)
BEGIN
DECLARE MINID INTEGER;
DECLARE MAXID INTEGER;
DECLARE TIID INTEGER;
DECLARE TSIID TEXT;
DECLARE ID INTEGER;
DECLARE IPDID INTEGER;
DECLARE TIPDID INTEGER;
DECLARE SUBTOTAL DECIMAL(15,2);
DECLARE SUBTOTAL1 DECIMAL(15,2);
DECLARE USERSTAMP_ID INTEGER;
DECLARE T_MATERIAL_COST DECIMAL(15,2);
DECLARE T_WIRING_COST DECIMAL(15,2);
DECLARE T_LABOUR_COST DECIMAL(15,2);
DECLARE EXIT HANDLER FOR SQLEXCEPTION
BEGIN
	ROLLBACK;
	SET SUCCESS_MESSAGE=0;
END;

START TRANSACTION;
CALL SP_LCE_CHANGE_USERSTAMP_AS_ULDID(USERSTAMP,@ULD_ID);
SET USERSTAMP_ID=@ULD_ID;

SET AUTOCOMMIT=0;
SET MINID=(SELECT MIN(TMD_ID) FROM LCE_TENDER_SUMMARY_DETAILS WHERE TM_ID=TMID);
SET MAXID=(SELECT MAX(TMD_ID) FROM LCE_TENDER_SUMMARY_DETAILS WHERE TM_ID=TMID);


	WHILE MINID <= MAXID DO
		IF EXISTS(SELECT TMD_ID FROM LCE_TENDER_SUMMARY_DETAILS WHERE TM_ID=TMID AND TMD_ID=MINID) THEN
			SET IPDID=(SELECT IPD_ID FROM LCE_TENDER_SUMMARY_DETAILS WHERE TMD_ID=MINID);
			SET TIPDID=(SELECT TIPD_ID FROM LCE_TENDER_SUMMARY_DETAILS WHERE TMD_ID=MINID); 
			IF IPDID IS NOT NULL THEN
			SET TIID=(SELECT TI_ID FROM LCE_ITEM_PRICE_DETAILS WHERE IPD_ID=(SELECT IPD_ID FROM LCE_TENDER_SUMMARY_DETAILS WHERE TMD_ID=MINID));
			END IF;
			IF TIPDID IS NOT NULL THEN
			SET TIID=(SELECT TI_ID FROM LCE_TENDER_ITEM_PRICE_DETAILS WHERE TIPD_ID=TIPDID);
			END IF;
			IF TIID=1 THEN
				UPDATE LCE_TENDER_SUMMARY_DETAILS SET TMD_MARK_DOWN=MARKDOWN, TMD_TENDER_MATERIAL_COST=(((TMD_QUANTITY*TMD_UNIT_RATE)+TMD_CPF)+((TMD_QUANTITY*TMD_UNIT_RATE)+TMD_CPF)*MARKDOWN/100),TMD_TENDER_PRICE=TMD_TENDER_MATERIAL_COST WHERE TMD_ID=MINID;
			ELSEIF (TIID=11) OR (TIID=12) OR (TIID=13) THEN
				UPDATE LCE_TENDER_SUMMARY_DETAILS SET TMD_MARK_DOWN=MARKDOWN, TMD_TENDER_WIRING_COST=((TMD_QUANTITY*TMD_WIRING_COST)+((TMD_QUANTITY*TMD_WIRING_COST)*MARKDOWN/100)),TMD_TENDER_PRICE=TMD_TENDER_WIRING_COST WHERE TMD_ID=MINID;
			ELSEIF (TIID=30) THEN
				UPDATE LCE_TENDER_SUMMARY_DETAILS SET TMD_MARK_DOWN=MARKDOWN,TMD_TENDER_MATERIAL_COST=((TMD_MATERIAL_COST)+(TMD_MATERIAL_COST)*MARKDOWN/100),TMD_TENDER_PRICE=TMD_TENDER_MATERIAL_COST WHERE TMD_ID=MINID;
			ELSE
				UPDATE LCE_TENDER_SUMMARY_DETAILS SET TMD_MARK_DOWN=MARKDOWN,TMD_TENDER_MATERIAL_COST=((TMD_QUANTITY*TMD_MATERIAL_COST)+((TMD_QUANTITY*TMD_MATERIAL_COST)*MARKDOWN/100)),TMD_TENDER_WIRING_COST=(TMD_WIRING_COST+(TMD_WIRING_COST*MARKDOWN/100)),TMD_TENDER_LABOUR_COST=(TMD_LABOUR_COST+(TMD_LABOUR_COST*MARKDOWN/100)) WHERE TMD_ID=MINID;
				SET T_MATERIAL_COST=(SELECT IF(TMD_TENDER_MATERIAL_COST IS NULL,'0.00',TMD_TENDER_MATERIAL_COST) FROM LCE_TENDER_SUMMARY_DETAILS WHERE TMD_ID=MINID);
				SET T_WIRING_COST=(SELECT IF(TMD_TENDER_WIRING_COST IS NULL,'0.00',TMD_TENDER_WIRING_COST) FROM LCE_TENDER_SUMMARY_DETAILS WHERE TMD_ID=MINID) ;
				SET T_LABOUR_COST=(SELECT IF(TMD_TENDER_LABOUR_COST IS NULL,'0.00',TMD_TENDER_LABOUR_COST) FROM LCE_TENDER_SUMMARY_DETAILS WHERE TMD_ID=MINID) ;
				UPDATE LCE_TENDER_SUMMARY_DETAILS SET TMD_TENDER_PRICE=(T_MATERIAL_COST+T_WIRING_COST+T_LABOUR_COST) WHERE TMD_ID=MINID;
			END IF;
		END IF;
		SET MINID=MINID+1;
	END WHILE;

SET TSIID=(SELECT DISTINCT(GROUP_CONCAT( TSI_ID)) FROM LCE_TENDER_SUB_ITEMS WHERE TSI_FLAG IS NULL);

MAIN_LOOP : LOOP
	CALL SP_LCE_GET_SPECIAL_CHARACTER_SEPERATED_VALUES(',',TSIID,@VALUE,@REMAINING_STRING);
		SELECT @VALUE INTO ID;
		SELECT @REMAINING_STRING INTO TSIID;
		SET TIID=(SELECT TI_ID FROM LCE_TENDER_SUB_ITEMS WHERE TSI_ID=ID);
		SET SUBTOTAL=(SELECT SUM(TMD_TENDER_PRICE) FROM LCE_TENDER_SUMMARY_DETAILS TSD,LCE_ITEM_PRICE_DETAILS IPD WHERE TSD.TM_ID=TMID AND TSD.IPD_ID=IPD.IPD_ID AND IPD.TSI_ID=ID);
		IF SUBTOTAL IS NULL THEN
			SET SUBTOTAL=0;
		END IF;
		SET SUBTOTAL1=(SELECT SUM(TMD_TENDER_PRICE) FROM LCE_TENDER_SUMMARY_DETAILS TSD,LCE_TENDER_ITEM_PRICE_DETAILS TIPD WHERE TSD.TM_ID=TMID AND TSD.TIPD_ID=TIPD.TIPD_ID AND TIPD.TSI_ID=ID);
		IF SUBTOTAL1 IS NULL THEN
			SET SUBTOTAL1=0;
		END IF;
		SET SUBTOTAL=(SUBTOTAL+SUBTOTAL1);
		IF SUBTOTAL IS NULL THEN
			SET SUBTOTAL=0;
		END IF;
		IF NOT EXISTS(SELECT TSI_ID FROM LCE_TENDER_SUB_TOTAL WHERE TM_ID=TMID AND TSI_ID=ID) THEN
			INSERT INTO LCE_TENDER_SUB_TOTAL(TM_ID,TI_ID,TSI_ID,TST_TENDER_PRICE,ULD_ID)VALUES
			(TMID,TIID,ID,SUBTOTAL,USERSTAMP_ID);
		ELSE
			UPDATE LCE_TENDER_SUB_TOTAL SET TST_TENDER_PRICE=SUBTOTAL,TST_AFTER_TRANSFER=(TST_TENDER_PRICE+TST_COST_TRANSFER),ULD_ID=USERSTAMP_ID WHERE TM_ID=TMID AND TSI_ID=ID AND TI_ID=TIID;
		END IF;

		IF TSIID='' IS NULL OR  TSIID='' THEN
			LEAVE  MAIN_LOOP;
		END IF;
END LOOP;
SET SUCCESS_MESSAGE=1;
COMMIT;
END;