

-- TENDER_SUMMARY

CREATE OR REPLACE VIEW VW_LCE_TENDER_SUMMARY AS SELECT IPD_ID,TMD_SNO,TMD_DESCRIPTION,IF(TMD_QUANTITY IS NOT NULL,(TMD_QUANTITY*TMD_MATERIAL_COST),TMD_MATERIAL_COST)AS MATERIAL_COST,IF (IPD_ID BETWEEN 1264 AND 1485,(TMD_QUANTITY*TMD_WIRING_COST),TMD_WIRING_COST) AS WIRING_COST,TMD_LABOUR_COST AS LABOUR_COST,TMD_TENDER_MATERIAL_COST AS TENDER_MATERIAL_COST,TMD_TENDER_WIRING_COST AS TENDER_WIRING_COST,TMD_TENDER_LABOUR_COST AS TENDER_LABOUR_COST,TMD_BASE_PRICE AS Tender_Base_Cost,TMD_TENDER_PRICE AS Tender_Submission_Price FROM lce_tender_summary_details WHERE TM_ID=1
